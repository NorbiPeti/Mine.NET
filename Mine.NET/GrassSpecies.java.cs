namespace Mine.NET
{
    /**
     * Represents the different types of grass.
     */
    public enum GrassSpecies
    {

        /**
         * Represents the dead looking grass.
         */
        DEAD = 0x0,
        /**
         * Represents the normal grass species.
         */
        NORMAL = 0x1,
        /**
         * Represents the fern-looking grass species.
         */
        FERN_LIKE = 0x2
    }
}
